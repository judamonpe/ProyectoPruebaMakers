package com.qa.certification.makerstest.models;

public class Total {
	
	
	private String date;
	private String portafolio;
	private int  nominal;
	private float price;
	private float total;
	
	public String getDate() {
		return date;
	}
	public String getPortafolio() {
		return portafolio;
	}
	public int getNominal() {
		return nominal;
	}
	public float getPrice() {
		return price;
	}
	public float getTotal() {
		return total;
	}

	

}
